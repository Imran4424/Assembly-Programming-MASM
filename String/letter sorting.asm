org 100h
.model small
.stack 100h
.data
MSG DB "Please Input Your String: $"
MSG2 DB 10,13,"AFTER SORTING: $"
MSG3 DB 10,13,"IN ASCENDING: $"
MSG4 DB 10,13,"IN DECENDING: $"

STRING DB 100 DUP(?)
STRING2 DB 100 DUP(?)

.code
MAIN PROC

	MOV AX,@DATA
	MOV DS,AX

	MOV AH,9
	LEA DX,MSG
	INT 21H

	MOV SI,0
	MOV AH,1

	WHILE:
		INT 21H
		CMP AL,13
		JE END_WHILE

		MOV STRING[SI],AL
		MOV STRING2[SI],AL
		INC SI
		JMP WHILE

	END_WHILE:
		MOV STRING[SI],'$'
		MOV STRING2[SI],'$'

		DEC SI
		MOV BX,SI
		MOV CX,SI

	SORT:
		MOV SI,0
		MOV DI,1

		FOR:
			CMP DI,BX
			JG END_FOR

			MOV AL,STRING[SI]
			CMP AL,STRING[DI]   ; ascending check
			JL POS1

			MOV DL,STRING[DI]

			MOV STRING[SI],DL
			MOV STRING[DI],AL

			POS1:
				MOV AL,STRING2[SI]
				CMP AL,STRING[DI]  ; decending check
				JG POS2

				MOV DL,STRING2[DI]
				MOV STRING2[SI],DL
				MOV STRING2[DI],AL

			POS2:
				INC SI
				INC DI
				JMP FOR

		END FOR:

	LOOP SORT

	OUTPUT:
		MOV AH,9
		LEA DX,MSG2
		INT 21H

		LEA DX,MSG3
		INT 21H

		LEA DX,STRING
		INT 21H

		LEA DX,MSG4
		INT 21H

		LEA DX,STRING2
		INT 21H

	MOV AH,4CH
	INT 21H

	MAIN ENDP
END MAIN